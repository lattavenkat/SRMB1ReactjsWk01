console.log("ADDITION, SUBTRACTION, MULTIPLICATIO USING ARROW FUNCTION");
let num1 = 100;
let num2 = 50;

const add = () => num1 + num2;
const sub = () => num1 - num2;
const mul = () => num1 * num2;

console.log("ADDING 100 AND 50", add());
console.log("SUBTRACTING 100 AND 50", sub());
console.log("MULTIPLYING 100 AND 50", mul());
